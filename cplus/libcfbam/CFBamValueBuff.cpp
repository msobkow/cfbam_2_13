	CFBamValueBuff::CFBamValueBuff()
	: cflib::ICFLibCloneableObj()
	{
		requiredRevision = 1;
		for( int i = 0; i < 16; i++ ) {
			createdByUserId[i] = 0;
		}
		createdAt = cflib::CFLib::getUTCTimestampInstance();
		for( int i = 0; i < 16; i++ ) {
			updatedByUserId[i] = 0;
		}
		updatedAt = cflib::CFLib::getUTCTimestampInstance();
		requiredTenantId = cfbam::CFBamValueBuff::TENANTID_INIT_VALUE;
		requiredScopeId = cfbam::CFBamValueBuff::SCOPEID_INIT_VALUE;
		requiredId = cfbam::CFBamValueBuff::ID_INIT_VALUE;
		optionalDefSchemaTenantId.setNull();
		optionalDefSchemaId.setNull();
		requiredName = new std::string( cfbam::CFBamValueBuff::NAME_INIT_VALUE );
		optionalShortName = NULL;
		optionalLabel = NULL;
		optionalShortDescription = NULL;
		optionalDescription = NULL;
		optionalDefaultXmlValue = NULL;
		requiredIsNullable = cfbam::CFBamValueBuff::ISNULLABLE_INIT_VALUE;
		optionalGenerateId.setNull();
		requiredImplementsPolymorph = cfbam::CFBamValueBuff::IMPLEMENTSPOLYMORPH_INIT_VALUE;
		optionalPrevTenantId.setNull();
		optionalPrevId.setNull();
		optionalNextTenantId.setNull();
		optionalNextId.setNull();
	}

